public class TypeCasting{
    public static void main(String[] args) {
        double d = 9.78;
        int i = (int) d;
        System.out.println("Double to int: " + d + " -> " + i);

        int x = 45;
        double y = (double) x;
        System.out.println("Int to double: " + x + " -> " + y);
    }
}